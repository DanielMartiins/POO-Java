package Exercicio2;

public class Duracao {

    final private int minutos;
    final private int segundos;

    public Duracao(int minutos, int segundos) {
        this.minutos = minutos;
        this.segundos = segundos;
    }

    public int getMinutos() {
        return minutos;
    }

    public int getSegundos() {
        return segundos;
    }
}
